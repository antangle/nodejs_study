var chineseArr = [];
var koreanArr = [];

for (var key in chinese) {
    if (chinese.hasOwnProperty(key)) {
        chineseArr.push( [ key, chinese[key] ] );
    }
}

for (var key in korean) {
    if (korean.hasOwnProperty(key)) {
        koreanArr.push( [ key, korean[key] ] );
    }
}

function shuffle(sourceArray) {
    for (var i = 0; i < sourceArray.length - 1; i++) {
        var j = i + Math.floor(Math.random() * (sourceArray.length - i));

        var temp = sourceArray[j];
        sourceArray[j] = sourceArray[i];
        sourceArray[i] = temp;
    }
    return sourceArray;
}

var shuffleChinese = shuffle(chineseArr);
var shuffleKorean = shuffle(koreanArr);

